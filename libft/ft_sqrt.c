int		ft_sqrt(int nb)
{
	int xn;
	int b;
	int i;

	i = 1;
	b = nb;
	xn = 2;
	if (nb <= 0)
		return (0);
	while (i < 25)
	{
		b = nb / xn;
		xn = (xn + b) / 2;
		i++;
	}
	if (xn * xn == nb)
		return (xn);
	else
		return (0);
}

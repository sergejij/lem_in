#include <unistd.h>
#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	if (s != 0 && fd >= 0)
	{
		while (*s != 0)
		{
			ft_putchar_fd(*s, fd);
			++s;
		}
	}
}

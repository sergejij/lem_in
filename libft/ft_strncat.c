#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t j;

	i = 0;
	j = 0;
	while (1)
	{
		if (s1[i] == '\0')
		{
			while (j < n && s2[j] != '\0')
			{
				s1[i] = s2[j];
				++i;
				++j;
			}
			s1[i] = '\0';
			return (s1);
		}
		++i;
	}
}
